        /// </summary>
        public const string CUBIC_MILLIMETER = "CUBIC_MILLIMETER";

        /// <summary>
        /// Change of geometric volume per second
        /// </summary>
        public const string CUBIC_MILLIMETER_PER_SECOND = "CUBIC_MILLIMETER/SECOND";

